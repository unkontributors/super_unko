function message() {
  name="$1"
  status="$2"
  expect="$3"
  
  [[ "$status" == "$3" ]] && echo "[OK] $name" && return 0

  echo "[NG] $name" 
  exit 1
}

function EQ() {
  name="$1"
  expect="$2"
  actual="$3"

  [[ "$expect" == "$actual" ]] 

  message $name $? 0
}

function OK() {
  name="$1"
  shift
  cmd="$@"

  $cmd &> /dev/null
  message $name $? 0
}

function NG() {
  name="$1"
  shift
  cmd="$@"

  $cmd &> /dev/null
  message $name $? 1
}

